          .thenAnswer((_) async => tTvModelList);
      // act
      final result = await repository.getNowPlayingTvs();
      // assert
      verify(mockRemoteDataSource.getNowPlayingTvs());
      final resultList = result.getOrElse(() => []);
      expect(resultList, tTvModelList);
    });

